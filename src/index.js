const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let arr = expr.split('**********'),
        arrLength = arr.length,
        str = '';
    for (let i = 0; i < arrLength; i++) {
        for (let j = 0; j < arr[i].length; j += 10) {
            let sl = arr[i].slice(j, j + 10).replace(/10/g, '.').replace(/11/g, '-').replace(/00/g, '');
            str += MORSE_TABLE[sl];
        }
        str += ' ';
    }
    return str.trim();
}

module.exports = {
    decode
}